// This java code is the demostration of the output methods are used in java.
class Output{
    public static void main(String[] args){
        // In java there are 3 methods in which the output is printed.
        //Output 1:"print"
        // this method will only print the string and next string also printed in the same line.
        System.out.print("This is print method");

        //output 2:"println" 
        // this method is used when we need to move the  courser to the next line.
        System.out.println(" This is println method");

        //Output 3:"\n"
        // this method also used to print the string to next line.
        System.out.print("This is \nslash n method");
    }
}