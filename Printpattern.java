// In this java code we are just printing the following pattern.
// *
// **
// *** 
// **** 
// ***** 
 class pattern{
    public static void main(String[] args){
        //Method 1
        System.out.println("This is one method:");
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");

        //Method 2
        System.out.println("This is another method:");
        System.out.print("*\n");
        System.out.print("**\n");
        System.out.print("***\n");
        System.out.print("****\n");
        System.out.print("*****\n");
    }
}